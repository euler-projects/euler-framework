package org.eulerframework.web.module.authentication;

/**
 * @author cFrost
 *
 */
public class ExpireableToken<T> {
    private T token;
    private long iat;
    private long exp;

    public T getToken() {
        return token;
    }

    public void setToken(T token) {
        this.token = token;
    }

    public long getIat() {
        return iat;
    }

    public void setIat(long iat) {
        this.iat = iat;
    }

    public long getExp() {
        return exp;
    }

    public void setExp(long exp) {
        this.exp = exp;
    }
}
